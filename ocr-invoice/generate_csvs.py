"""
新規10枚のPDF納品書からCSVを生成する（一時スクリプト）
"""
import csv, os

script_dir = os.path.dirname(os.path.abspath(__file__))
results_dir = os.path.join(script_dir, 'results')
master_path = os.path.join(script_dir, '商品_utf8.csv')

# --- マスター読み込み ---
jan_map  = {}  # JAN -> (code, name)
aite_map = {}  # 相手商品CD -> (code, name)

with open(master_path, encoding='utf-8-sig') as f:
    for row in csv.DictReader(f):
        code = row['コード'].strip()
        name = row['商品名'].strip()
        jan  = row['JANCD'].strip()
        aite = row['相手商品CD'].strip()
        if jan:  jan_map[jan]   = (code, name)
        if aite: aite_map[aite] = (code, name)

def by_napra(napra_code, ocr_name, qty, price, total):
    r = aite_map.get(str(napra_code))
    if r:
        return [r[0], r[1], qty, price, total]
    return ['1', ocr_name, qty, price, total]

def by_jan(jan, ocr_name, qty, price, total):
    r = jan_map.get(jan)
    if r:
        return [r[0], r[1], qty, price, total]
    return ['1', ocr_name, qty, price, total]

def by_name(ocr_name, qty, price, total):
    return ['1', ocr_name, qty, price, total]

HEADER = ['コード', '商品名', '数量', '単価', '合計']

def write_csv(fname, rows):
    path = os.path.join(results_dir, fname)
    with open(path, 'w', encoding='utf-8-sig', newline='') as f:
        w = csv.writer(f)
        w.writerow(HEADER)
        for r in rows:
            w.writerow(r)
    print(f'  OK: {fname} ({len(rows)}行)')

